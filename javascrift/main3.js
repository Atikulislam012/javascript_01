var digit = prompt("Enter your digit : ")

switch (digit){
    case "0":
        alert("Zero");
        break;
    case "1":
        alert("One");
        break;
    case "2":
        alert("two");
        break;
    case "3":
        alert("Three");
        break;
    case "4":
        alert("Four");
        break;
    case "5":
        alert("Five");
        break;
    case "6":
        alert("Six");
        break;
    case "7":
        alert("Seven");
        break;
    case "8":
        alert("Eaight");
        break;
    case "9":
        alert("Nine");
        break;
    default:
        alert("Not a valid digit");
}

